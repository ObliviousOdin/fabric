@dataclass
class _QuickSnapshotWriter:
    root: Path
    snapshot_id: str
    path: Path
    root_fd: int | None = None
    snapshot_fd: int | None = None
    windows_handles: list[int] = field(default_factory=list)

    def _relative_parts(self, relative: str | Path) -> tuple[str, ...]:
        candidate = Path(relative)
        if candidate.is_absolute() or any(
            part in {"", ".", ".."} for part in candidate.parts
        ):
            raise PermissionError("quick snapshot path escaped its attempt root")
        return candidate.parts

    def _posix_parent(self, parts: tuple[str, ...]) -> tuple[int, str]:
        if self.snapshot_fd is None:
            raise PermissionError("quick snapshot root is not pinned")
        directory_flags = os.O_RDONLY | getattr(os, "O_DIRECTORY", 0)
        directory_flags |= getattr(os, "O_CLOEXEC", 0)
        directory_flags |= getattr(os, "O_NOFOLLOW", 0)
        current = os.dup(self.snapshot_fd)
        try:
            for part in parts[:-1]:
                try:
                    os.mkdir(part, 0o700, dir_fd=current)
                except FileExistsError:
                    pass
                child = os.open(part, directory_flags, dir_fd=current)
                os.fchmod(child, 0o700)
                opened = os.fstat(child)
                visible = os.stat(part, dir_fd=current, follow_symlinks=False)
                if (
                    not stat.S_ISDIR(opened.st_mode)
                    or (opened.st_dev, opened.st_ino)
                    != (visible.st_dev, visible.st_ino)
                    or stat.S_IMODE(opened.st_mode) != 0o700
                ):
                    os.close(child)
                    raise PermissionError("quick snapshot subdirectory changed")
                os.close(current)
                current = child
            return current, parts[-1]
        except BaseException:
            os.close(current)
            raise

    def create_file(self, relative: str | Path) -> tuple[Path, int]:
        parts = self._relative_parts(relative)
        target = self.path.joinpath(*parts)
        if target.parent == self.root or target == self.root:
            raise PermissionError("quick snapshot file escaped its attempt directory")
        if os.name == "nt":  # pragma: no cover - native Windows CI
            from fabric_cli.provider_accounts import (
                _windows_pin_directory_tree,
                _windows_private_directory,
            )

            current = self.path
            for part in parts[:-1]:
                current /= part
                try:
                    current.mkdir(mode=0o700)
                except FileExistsError:
                    pass
                if not _windows_private_directory(current, apply=True):
                    raise PermissionError("quick snapshot subdirectory is not private")
                handles, _identity = _windows_pin_directory_tree(current)
                self.windows_handles.extend(handles)
            return target, _create_private_file(target)

        parent_fd, name = self._posix_parent(parts)
        try:
            flags = os.O_RDWR | os.O_CREAT | os.O_EXCL
            flags |= getattr(os, "O_CLOEXEC", 0)
            flags |= getattr(os, "O_NOFOLLOW", 0)
            descriptor = os.open(name, flags, 0o600, dir_fd=parent_fd)
            os.fchmod(descriptor, 0o600)
            opened = os.fstat(descriptor)
            visible = os.stat(name, dir_fd=parent_fd, follow_symlinks=False)
            if (
                not stat.S_ISREG(opened.st_mode)
                or getattr(opened, "st_nlink", 1) != 1
                or (opened.st_dev, opened.st_ino)
                != (visible.st_dev, visible.st_ino)
                or stat.S_IMODE(opened.st_mode) != 0o600
            ):
                os.close(descriptor)
                raise PermissionError("quick snapshot file changed")
            return target, descriptor
        finally:
            os.close(parent_fd)

    def copy_file(self, source: Path, relative: str | Path) -> int:
        target, descriptor = self.create_file(relative)
        _copy_to_private_descriptor(source, descriptor)
        return self.file_size(relative, target=target)

    def copy_database(self, source: Path, relative: str | Path) -> int | None:
        if _is_canonical_work_store_relative(relative):
            try:
                target, descriptor = self.create_file(relative)
            except (OSError, PermissionError) as exc:
                raise WorkStoreSnapshotError(
                    "destination_unavailable",
                    f"cannot create quick Work snapshot destination: {exc}",
                ) from exc
            if snapshot_work_db_to_disk(
                source,
                target,
                _destination_fd=descriptor,
            ) is None:
                raise WorkStoreSnapshotError(
                    "source_disappeared",
                    "Work store disappeared after quick-backup enumeration",
                )
            return self.file_size(relative, target=target)

        target, descriptor = self.create_file(relative)
        if not _safe_copy_db(source, target, _destination_fd=descriptor):
            return None
        return self.file_size(relative, target=target)

    def write_bytes(self, relative: str | Path, payload: bytes) -> None:
        _target, descriptor = self.create_file(relative)
        try:
            with os.fdopen(descriptor, "wb") as writer:
                descriptor = -1
                writer.write(payload)
                writer.flush()
                os.fsync(writer.fileno())
        finally:
            if descriptor >= 0:
                with contextlib.suppress(OSError):
                    os.close(descriptor)

    def file_size(self, relative: str | Path, *, target: Path | None = None) -> int:
        parts = self._relative_parts(relative)
        if os.name == "nt":  # pragma: no cover - native Windows CI
            return (target or self.path.joinpath(*parts)).stat().st_size
        parent_fd, name = self._posix_parent(parts)
        try:
            metadata = os.stat(name, dir_fd=parent_fd, follow_symlinks=False)
            if not stat.S_ISREG(metadata.st_mode):
                raise PermissionError("quick snapshot file changed")
            return metadata.st_size
        finally:
            os.close(parent_fd)


@contextmanager
def _quick_snapshot_writer(root: Path, timestamp: str) -> Iterator[_QuickSnapshotWriter]:
    """Create one collision-proof direct-child snapshot attempt and pin it."""
    _ensure_private_directory(root)
    root_fd: int | None = None
    snapshot_fd: int | None = None
    windows_handles: list[int] = []
    writer: _QuickSnapshotWriter | None = None
    try:
        for _attempt in range(32):
            snapshot_id = f"{timestamp}-{uuid.uuid4().hex}"
            snapshot_path = root / snapshot_id
            if snapshot_path.parent != root:
                raise PermissionError("quick snapshot id escaped its root")
            try:
                if os.name == "nt":  # pragma: no cover - native Windows CI
                    from fabric_cli.provider_accounts import (
                        _windows_pin_directory_tree,
                        _windows_private_directory,
                    )

                    if not windows_handles:
                        root_handles, _root_identity = _windows_pin_directory_tree(root)
                        windows_handles.extend(root_handles)
                    snapshot_path.mkdir(mode=0o700)
                    if not _windows_private_directory(snapshot_path, apply=True):
                        raise PermissionError("quick snapshot directory is not private")
                    child_handles, _child_identity = _windows_pin_directory_tree(
                        snapshot_path
                    )
                    windows_handles.extend(child_handles)
                else:
                    directory_flags = os.O_RDONLY | getattr(os, "O_DIRECTORY", 0)
                    directory_flags |= getattr(os, "O_CLOEXEC", 0)
                    directory_flags |= getattr(os, "O_NOFOLLOW", 0)
                    if root_fd is None:
                        root_fd = os.open(root, directory_flags)
                        opened_root = os.fstat(root_fd)
                        visible_root = os.stat(root, follow_symlinks=False)
                        if (
                            not stat.S_ISDIR(opened_root.st_mode)
                            or (opened_root.st_dev, opened_root.st_ino)
                            != (visible_root.st_dev, visible_root.st_ino)
                        ):
                            raise PermissionError("quick snapshot root changed")
                    os.mkdir(snapshot_id, 0o700, dir_fd=root_fd)
                    snapshot_fd = os.open(
                        snapshot_id,
                        directory_flags,
                        dir_fd=root_fd,
                    )
                    os.fchmod(snapshot_fd, 0o700)
                    opened_child = os.fstat(snapshot_fd)
                    visible_child = os.stat(
                        snapshot_id,
                        dir_fd=root_fd,
                        follow_symlinks=False,
                    )
                    if (
                        not stat.S_ISDIR(opened_child.st_mode)
                        or (opened_child.st_dev, opened_child.st_ino)
                        != (visible_child.st_dev, visible_child.st_ino)
                        or stat.S_IMODE(opened_child.st_mode) != 0o700
                    ):
                        raise PermissionError("quick snapshot attempt changed")
            except FileExistsError:
                continue
            writer = _QuickSnapshotWriter(
                root=root,
                snapshot_id=snapshot_id,
                path=snapshot_path,
                root_fd=root_fd,
                snapshot_fd=snapshot_fd,
                windows_handles=windows_handles,
            )
            yield writer
            return
        raise FileExistsError("could not allocate a unique quick snapshot attempt")
    finally:
        if snapshot_fd is not None:
            with contextlib.suppress(OSError):
                os.close(snapshot_fd)
        if root_fd is not None:
            with contextlib.suppress(OSError):
                os.close(root_fd)
        if windows_handles:
            from fabric_cli.provider_accounts import _windows_close_handle

            for handle in reversed(windows_handles):
                _windows_close_handle(handle)


def create_quick_snapshot(
    label: Optional[str] = None,
    fabric_home: Optional[Path] = None,
    keep: Optional[int] = None,
) -> Optional[str]:
    """Create a quick state snapshot of critical files.

    Copies STATE_FILES to a timestamped directory under state-snapshots/.
    Auto-prunes old snapshots beyond the keep limit.

    Returns:
        Snapshot ID (timestamp-based), or None if no files found.
    """
    home = fabric_home or get_fabric_home()
    root = _quick_snapshot_root(home)

    ts = datetime.now(timezone.utc).strftime("%Y%m%d-%H%M%S-%f")
    with _quick_snapshot_writer(root, ts) as writer:
        manifest: Dict[str, int] = {}

        for rel in _QUICK_STATE_FILES:
            src = home / rel
            if not src.exists():
                continue

            if src.is_dir():
                # Walk the directory and record each file individually in the
                # manifest so restore can treat them uniformly. Empty dirs are
                # skipped (nothing to snapshot).
                for sub in src.rglob("*"):
                    if not sub.is_file():
                        continue
                    sub_rel = sub.relative_to(home).as_posix()
                    if (
                        "/workspaces/" in f"/{sub_rel}/"
                        or "/attachments/" in f"/{sub_rel}/"
                    ):
                        continue
                    try:
                        if sub.suffix == ".db":
                            size = writer.copy_database(sub, sub_rel)
                            if size is None:
                                continue
                        else:
                            size = writer.copy_file(sub, sub_rel)
                        manifest[sub_rel] = size
                    except WorkStoreSnapshotError:
                        raise
                    except (OSError, PermissionError) as exc:
                        logger.warning("Could not snapshot %s: %s", sub_rel, exc)
                continue

            if not src.is_file():
                continue

            try:
                if src.suffix == ".db":
                    size = writer.copy_database(src, rel)
                    if size is None:
                        continue
                else:
                    size = writer.copy_file(src, rel)
                manifest[rel] = size
            except WorkStoreSnapshotError:
                raise
            except (OSError, PermissionError) as exc:
                logger.warning("Could not snapshot %s: %s", rel, exc)

        if not manifest:
            # The random attempt directory is intentionally left private and
            # manifest-less. Deleting by pathname after a failure could remove
            # an attacker substitution; list/restore ignore incomplete attempts.
            return None

        snap_id = writer.snapshot_id
        meta = {
            "id": snap_id,
            "timestamp": ts,
            "label": label,
            "file_count": len(manifest),
            "total_size": sum(manifest.values()),
            "files": manifest,
        }
        writer.write_bytes(
            "manifest.json",
            (json.dumps(meta, indent=2) + "\n").encode("utf-8"),
        )

    # Auto-prune. Defaults preserve historical manual /snapshot behavior; callers
    # with known high-churn safety snapshots (for example pre-update) can pass a
    # smaller keep value so large state.db copies do not accumulate indefinitely.
    _prune_quick_snapshots(root, keep=_QUICK_DEFAULT_KEEP if keep is None else keep)

    logger.info("State snapshot created: %s (%d files)", snap_id, len(manifest))
    return snap_id


def list_quick_snapshots(
    limit: int = 20,
    fabric_home: Optional[Path] = None,
) -> List[Dict[str, Any]]:
    """List existing quick state snapshots, most recent first."""
    root = _quick_snapshot_root(fabric_home)
    if not root.exists():
        return []

    results = []
    for d in sorted(root.iterdir(), reverse=True):
        if not d.is_dir():
            continue
        manifest_path = d / "manifest.json"
        if manifest_path.exists():
            try:
                with open(manifest_path, encoding="utf-8") as f:
                    results.append(json.load(f))
            except (json.JSONDecodeError, OSError):
                results.append({"id": d.name, "file_count": 0, "total_size": 0})
        if len(results) >= limit:
            break

    return results


def restore_quick_snapshot(
    snapshot_id: str,
    fabric_home: Optional[Path] = None,
) -> bool:
    """Restore state from a quick snapshot.

    Overwrites current state files with the snapshot's copies.
    Returns True if at least one file was restored.
    """
    home = fabric_home or get_fabric_home()
    root = _quick_snapshot_root(home)

    # Security: reject snapshot_id values that contain path separators or
    # traversal sequences so that `root / snapshot_id` stays inside root.
    if not snapshot_id or "/" in snapshot_id or "\\" in snapshot_id or snapshot_id in (".", ".."):
        logger.error("Invalid snapshot_id: %s", snapshot_id)
        return False

    snap_dir = root / snapshot_id

    # Confirm the resolved path is still inside root (handles symlinks etc.)
    try:
        snap_dir.resolve().relative_to(root.resolve())
    except ValueError:
        logger.error("Snapshot path traversal blocked for id: %s", snapshot_id)
        return False

    if not snap_dir.is_dir():
        return False

    manifest_path = snap_dir / "manifest.json"
    if not manifest_path.exists():
        return False

    with open(manifest_path, encoding="utf-8") as f:
        meta = json.load(f)

    restored = 0
    account_state_restored = False
    for rel in meta.get("files", {}):
        # Work-store replacement requires the cross-process lifecycle guard
        # specified by FMB-002.  Until that guard exists, quick restore is
        # intentionally backup-only for work.db and cannot replace a live
        # ledger behind WorkService's open connections.
        if _is_canonical_work_store_artifact_relative(rel):
            logger.warning(
                "Skipping %s: live Work-store restore requires lifecycle guard",
                rel,
            )
            continue
        # Security: reject absolute paths and traversals in manifest entries
        src = snap_dir / rel
        try:
            src.resolve().relative_to(snap_dir.resolve())
        except ValueError:
            logger.error("Manifest path traversal blocked: %s", rel)
            continue

        dst = home / rel
        try:
            dst.resolve().relative_to(home.resolve())
        except ValueError:
            logger.error("Manifest path traversal blocked: %s", rel)
            continue

        if not src.exists():
            continue

        dst.parent.mkdir(parents=True, exist_ok=True)

        try:
            if dst.suffix == ".db":
                # Atomic-ish replace for databases
                tmp = dst.parent / f".{dst.name}.snap_restore"
                shutil.copy2(src, tmp)
                dst.unlink(missing_ok=True)
                shutil.move(str(tmp), str(dst))
            else:
                shutil.copy2(src, dst)
            _tighten_provider_account_artifact(dst, home)
            if _restored_provider_account_home(dst, Path(home)) == Path(home):
                account_state_restored = True
            restored += 1
        except (OSError, PermissionError) as exc:
            logger.error("Failed to restore %s: %s", rel, exc)

    if account_state_restored:
        _rebind_restored_provider_account_homes({Path(home)})

    logger.info("Restored %d files from snapshot %s", restored, snapshot_id)
    return restored > 0


# Relative path of the cron job database inside FABRIC_HOME. Kept in sync with
# the entry in ``_QUICK_STATE_FILES`` and with ``cron/jobs.py``'s ``JOBS_FILE``.
_CRON_JOBS_REL = "cron/jobs.json"


def _count_cron_jobs(path: Path) -> Optional[int]:
    """Return the number of cron jobs stored in ``path``.

    The canonical on-disk shape is ``{"jobs": [...]}`` (see ``cron/jobs.py``).
    A legacy bare-list shape (``[...]``) is also honoured.

    Returns:
        The job count for any *valid, readable* JSON document, or ``None`` if
        the file is missing or cannot be parsed. ``None`` means "unknown" —
        callers must not treat it as "zero jobs", because acting on an
        unreadable file could mask a real corruption the user needs to see.
    """
    if not path.is_file():
        return None
    try:
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
    except (OSError, json.JSONDecodeError):
        return None
    if isinstance(data, dict):
        jobs = data.get("jobs", [])
        return len(jobs) if isinstance(jobs, list) else None
    if isinstance(data, list):
        return len(data)
    return None


def restore_cron_jobs_if_emptied(
    snapshot_id: str,
    fabric_home: Optional[Path] = None,
) -> Optional[Dict[str, Any]]:
    """Safety net for silent cron-job loss across ``fabric update``.

    Config-version migrations have been observed to leave ``cron/jobs.json``
    valid-but-empty after an update, silently dropping every scheduled job
    (issue #34600). The desktop scheduler can also overwrite the file with its
    own small set of internally-tracked crons, causing partial loss (issue
    #52144).

    This compares the *current* job count against the pre-update snapshot. If
    the live file now has **fewer** jobs than the snapshot, the snapshot copy
    of ``cron/jobs.json`` is restored in place.

    The check is deliberately conservative — it only ever restores when there
    is unambiguous evidence of loss (snapshot had more jobs than live file),
    so a user who genuinely deleted jobs during/after the update is never
    second-guessed, and an unreadable live file (count ``None``) is left
    untouched so real corruption still surfaces.

    Args:
        snapshot_id: The pre-update quick-snapshot id (from
            :func:`create_quick_snapshot`).
        fabric_home: Override for the Fabric home directory (tests).

    Returns:
        ``None`` when no action was taken (the common, healthy path). On a
        successful restore, a dict ``{"restored": True, "job_count": N,
        "snapshot_id": ...}`` so the caller can warn the user.
    """
    if not snapshot_id:
        return None

    home = fabric_home or get_fabric_home()
    live_path = home / _CRON_JOBS_REL

    live_count = _count_cron_jobs(live_path)
    # ``None`` (missing or unparseable) is intentionally left alone — that's a
    # different failure mode the user should see rather than have papered over.
    if live_count is None:
        return None

    snap_path = _quick_snapshot_root(home) / snapshot_id / _CRON_JOBS_REL
    snap_count = _count_cron_jobs(snap_path)
    if not snap_count:  # None or 0 — nothing worth restoring
        return None

    # Restore when live has FEWER jobs than the pre-update snapshot.
    # Catches both total loss (0 vs N) and partial loss (1 vs 19) — the
    # desktop scheduler can overwrite jobs.json with its own small set of
    # internally-tracked crons after an update/restart.
    if live_count >= snap_count:
        return None

    try:
        live_path.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(snap_path, live_path)
    except (OSError, PermissionError) as exc:
        logger.error(
            "Cron jobs were emptied during update but auto-restore failed: %s", exc
        )
        return None

    logger.warning(
        "Restored %d cron job(s) from pre-update snapshot %s "
        "(live file had %d job(s), snapshot had %d — jobs were lost during migration)",
        snap_count,
        snapshot_id,
        live_count,
        snap_count,
    )
    return {"restored": True, "job_count": snap_count, "snapshot_id": snapshot_id}


def _prune_quick_snapshots(root: Path, keep: int = _QUICK_DEFAULT_KEEP) -> int:
    """Remove oldest quick snapshots beyond the keep limit. Returns count deleted."""
    if not root.exists():
        return 0

    dirs = sorted(
        (d for d in root.iterdir() if d.is_dir()),
        key=lambda d: d.name,
        reverse=True,
    )

    deleted = 0
    for d in dirs[keep:]:
        try:
            shutil.rmtree(d)
            deleted += 1
        except OSError as exc:
            logger.warning("Failed to prune snapshot %s: %s", d.name, exc)

    return deleted


def prune_quick_snapshots(
    keep: int = _QUICK_DEFAULT_KEEP,
    fabric_home: Optional[Path] = None,
) -> int:
    """Manually prune quick snapshots. Returns count deleted."""
    return _prune_quick_snapshots(_quick_snapshot_root(fabric_home), keep=keep)


def run_quick_backup(args) -> None:
    """CLI entry point for fabric backup --quick."""
    label = getattr(args, "label", None)
    snap_id = create_quick_snapshot(label=label)
    if snap_id:
        print(f"State snapshot created: {snap_id}")
        snaps = list_quick_snapshots()
        print(f"  {len(snaps)} snapshot(s) stored in {display_fabric_home()}/state-snapshots/")
        print(f"  Restore with: /snapshot restore {snap_id}")
    else:
        print("No state files found to snapshot.")


