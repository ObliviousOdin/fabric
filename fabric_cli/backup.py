# Reserved archive subtree for provider state that lives OUTSIDE FABRIC_HOME
# (e.g. ~/.honcho, ~/.hindsight). The active memory provider declares these via
# MemoryProvider.backup_paths(); they're stored under this prefix encoded
# relative to the user's home directory, and restored to their original
# home-relative location on import. Anything not under home is skipped.
_EXTERNAL_PREFIX = "_external/"


def _collect_memory_provider_external_paths() -> List[Path]:
    """Return existing absolute paths the active memory provider stores
    outside FABRIC_HOME, resolved from config only (no network, no init).

    Reads ``memory.provider`` from config, loads just that provider, and asks
    it for ``backup_paths()``. Returns an empty list when no external provider
    is active or the provider can't be loaded — backup must never fail because
    of a flaky plugin.
    """
    try:
        from plugins.memory import _get_active_memory_provider, load_memory_provider
    except Exception:
        return []

    try:
        active = _get_active_memory_provider()
    except Exception:
        active = None
    if not active:
        return []

    try:
        provider = load_memory_provider(active)
    except Exception:
        provider = None
    if provider is None:
        return []

    try:
        declared = provider.backup_paths() or []
    except Exception as exc:
        logger.warning("backup_paths() failed for memory provider %r: %s", active, exc)
        return []

    out: List[Path] = []
    seen: set = set()
    for raw in declared:
        try:
            p = Path(raw).expanduser()
        except Exception:
            continue
        if not p.exists():
            continue
        try:
            resolved = p.resolve()
        except (OSError, ValueError):
            continue
        if resolved in seen:
            continue
        seen.add(resolved)
        out.append(p)
    return out


def _iter_external_files(base: Path) -> List[Path]:
    """Yield regular files under *base* (a file or a directory), skipping
    symlinks, caches, and pyc files. *base* itself may be a file."""
    files: List[Path] = []
    if base.is_file() and not base.is_symlink():
        files.append(base)
        return files
    if not base.is_dir():
        return files
    for dirpath, dirnames, filenames in os.walk(base, followlinks=False):
        dp = Path(dirpath)
        dirnames[:] = [d for d in dirnames if d not in _EXCLUDED_DIRS]
        for fname in filenames:
            fpath = dp / fname
            if fpath.is_symlink():
                continue
            if fpath.name in _EXCLUDED_NAMES or fpath.name.endswith(_EXCLUDED_SUFFIXES):
                continue
            files.append(fpath)
    return files


def _should_exclude(rel_path: Path) -> bool:
    """Return True if *rel_path* (relative to fabric root) should be skipped."""
    parts = rel_path.parts

    for part in parts:
        if part not in _EXCLUDED_DIRS:
            continue
        # ``fabric-agent`` only matches at the root level (first component).
        # Nested directories with the same name — e.g.
        # ``skills/autonomous-ai-agents/fabric-agent/`` — must be preserved.
        if part == "fabric-agent" and part != parts[0]:
            continue
        return True

    name = rel_path.name

    if name in _EXCLUDED_NAMES:
        return True

    # The consistent main Work snapshot is handled specially at archive time;
    # live WAL/SHM/journal and lifecycle coordination files never ship.
    if (
        name.casefold() != "work.db"
        and _is_canonical_work_store_artifact_relative(rel_path)
    ):
        return True

    if name.startswith(_PROVIDER_ACCOUNT_TEMP_FILE_PREFIX):
        return True

    if name.startswith(_PRIVATE_BACKUP_TEMP_PREFIX) and name.endswith(".zip"):
        return True

    if name.endswith(_EXCLUDED_SUFFIXES):
        return True

    return False


def _tighten_provider_account_artifact(path: Path, profile_home: Path) -> None:
    """Restore private modes for account state and repair artifacts.

    ``zipfile`` does not preserve Unix modes, and quick snapshots may be
    restored under a different umask. This helper is deliberately scoped to
    one explicit profile home; it never searches or merges sibling profiles.
    """
    try:
        rel = path.relative_to(profile_home)
    except ValueError:
        return

    if rel == Path(_PROVIDER_ACCOUNT_STATE_FILE):
        try:
            path.chmod(0o600)
        except OSError:
            pass
        return

    if not rel.parts or rel.parts[0] != _PROVIDER_ACCOUNT_REPAIR_DIR:
        return

    repair_root = profile_home / _PROVIDER_ACCOUNT_REPAIR_DIR
    current = repair_root
    try:
        current.chmod(0o700)
    except OSError:
        pass
    for part in rel.parts[1:-1]:
        current /= part
        try:
            current.chmod(0o700)
        except OSError:
            pass
    try:
        if path.is_dir():
            path.chmod(0o700)
        elif path.is_file():
            path.chmod(0o600)
    except OSError:
        pass


def _profile_home_for_backup_target(target: Path, fabric_root: Path) -> Path:
    """Resolve the exact profile home owning one restored archive target."""
    try:
        rel = target.relative_to(fabric_root)
    except ValueError:
        return fabric_root
    if len(rel.parts) >= 3 and rel.parts[0] == "profiles":
        return fabric_root / "profiles" / rel.parts[1]
    return fabric_root


def _restored_provider_account_home(
    target: Path,
    fabric_root: Path,
) -> Path | None:
    """Return the profile home only for an exact restored account-state path.

    Archive member names are attacker-controlled.  Matching the basename is
    insufficient because a nested file such as
    ``profiles/name/export/provider-accounts.json`` is not the live profile
    store.  Canonical relative-path matching also covers harmless internal
    ``.``/``..`` normalization without depending on profile enumeration.
    """
    forms: list[tuple[Path, Path]] = []
    try:
        forms.append((
            Path(os.path.abspath(os.fspath(target))),
            Path(os.path.abspath(os.fspath(fabric_root))),
        ))
    except (OSError, TypeError, ValueError):
        pass
    try:
        resolved = (target.resolve(), fabric_root.resolve())
        if resolved not in forms:
            forms.append(resolved)
    except (OSError, RuntimeError):
        pass

    for candidate, root in forms:
        try:
            rel = candidate.relative_to(root)
        except ValueError:
            continue
        if rel == Path(_PROVIDER_ACCOUNT_STATE_FILE):
            return fabric_root
        if (
            len(rel.parts) == 3
            and rel.parts[0] == "profiles"
            and rel.parts[1] not in {"", ".", ".."}
            and rel.parts[2] == _PROVIDER_ACCOUNT_STATE_FILE
        ):
            return fabric_root / "profiles" / rel.parts[1]
    return None


