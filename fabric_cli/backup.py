def _ensure_private_directory(path: Path) -> None:
    try:
        existing = path.stat(follow_symlinks=False)
    except FileNotFoundError:
        existing = None
    if existing is not None:
        reparse_flag = getattr(stat, "FILE_ATTRIBUTE_REPARSE_POINT", 0x400)
        if (
            not stat.S_ISDIR(existing.st_mode)
            or stat.S_ISLNK(existing.st_mode)
            or getattr(existing, "st_file_attributes", 0) & reparse_flag
        ):
            raise PermissionError("snapshot directory is redirected")
    path.mkdir(parents=True, exist_ok=True, mode=0o700)
    if os.name == "nt":  # pragma: no cover - native Windows CI
        from fabric_cli.provider_accounts import _windows_private_directory

        if not _windows_private_directory(path, apply=True):
            raise PermissionError("snapshot directory is not private")
        return
    path.chmod(0o700)
    metadata = path.stat(follow_symlinks=False)
    if not stat.S_ISDIR(metadata.st_mode) or stat.S_IMODE(metadata.st_mode) != 0o700:
        raise PermissionError("snapshot directory is not private")


def _ensure_private_subdirectory(base: Path, target: Path) -> None:
    _ensure_private_directory(base)
    try:
        relative = target.relative_to(base)
    except ValueError as exc:
        raise PermissionError("snapshot path escaped private root") from exc
    current = base
    for part in relative.parts:
        current /= part
        _ensure_private_directory(current)


def _create_private_file(path: Path) -> int:
    if os.name == "nt":  # pragma: no cover - native Windows CI
        from fabric_cli.provider_accounts import _windows_open_private_file

        return _windows_open_private_file(
            path,
            create_new=True,
            share_write=False,
        )
    flags = os.O_RDWR | os.O_CREAT | os.O_EXCL
    flags |= getattr(os, "O_CLOEXEC", 0)
    flags |= getattr(os, "O_NOFOLLOW", 0)
    descriptor = os.open(path, flags, 0o600)
    os.fchmod(descriptor, 0o600)
    opened = os.fstat(descriptor)
    if (
        not stat.S_ISREG(opened.st_mode)
        or getattr(opened, "st_nlink", 1) != 1
        or stat.S_IMODE(opened.st_mode) != 0o600
    ):
        os.close(descriptor)
        raise PermissionError("snapshot file is not private")
    return descriptor


def _harden_private_file(path: Path) -> None:
    if os.name == "nt":  # pragma: no cover - native Windows CI
        from fabric_cli.provider_accounts import _windows_open_private_file

        descriptor = _windows_open_private_file(path, create_new=False)
        os.close(descriptor)
        return
    path.chmod(0o600)
    if stat.S_IMODE(path.stat(follow_symlinks=False).st_mode) != 0o600:
        raise PermissionError("snapshot file is not private")


def _prepare_private_file(path: Path) -> None:
    descriptor = _create_private_file(path)
    os.close(descriptor)


def _copy_to_private_file(source: Path, target: Path) -> None:
    descriptor = _create_private_file(target)
    _copy_to_private_descriptor(source, descriptor)
    _harden_private_file(target)


def _copy_to_private_descriptor(source: Path, descriptor: int) -> None:
    try:
        with PinnedFileCapability.open(source) as capability:
            with capability.open_reader() as reader, os.fdopen(
                descriptor,
                "wb",
            ) as writer:
                descriptor = -1
                shutil.copyfileobj(reader, writer, length=1024 * 1024)
                writer.flush()
                os.fsync(writer.fileno())
    finally:
        if descriptor >= 0:
            with contextlib.suppress(OSError):
                os.close(descriptor)


def _private_db_staging_path(parent: Path) -> Path:
    return parent / f".fabric-private-db-{os.getpid()}-{uuid.uuid4().hex}.db"


@contextmanager
def _private_work_db_staging_path(parent: Path) -> Iterator[Path]:
    """Yield a UUID-scoped 0700 directory containing one Work snapshot path."""
    directory = parent / f".fabric-private-work-{uuid.uuid4().hex}"
    created: os.stat_result | None = None
    try:
        try:
            directory.mkdir(mode=0o700)
            created = directory.stat(follow_symlinks=False)
            _ensure_private_directory(directory)
        except (OSError, PermissionError) as exc:
            raise WorkStoreSnapshotError(
                "staging_unavailable",
                "Work snapshot staging directory is not private",
            ) from exc
        yield directory / "work.db"
    finally:
        try:
            current = directory.stat(follow_symlinks=False)
        except OSError:
            current = None
        if created is not None and current is not None and (
            current.st_dev,
            current.st_ino,
        ) == (created.st_dev, created.st_ino):
            with contextlib.suppress(OSError):
                directory.rmdir()


def _private_destination_matches(descriptor: int, path: Path) -> bool:
    try:
        opened = os.fstat(descriptor)
        current = os.stat(path, follow_symlinks=False)
    except OSError:
        return False
    reparse_flag = getattr(stat, "FILE_ATTRIBUTE_REPARSE_POINT", 0x400)
    if (
        not stat.S_ISREG(opened.st_mode)
        or not stat.S_ISREG(current.st_mode)
        or stat.S_ISLNK(current.st_mode)
        or getattr(current, "st_file_attributes", 0) & reparse_flag
        or getattr(opened, "st_nlink", 1) != 1
        or (opened.st_dev, opened.st_ino) != (current.st_dev, current.st_ino)
    ):
        return False
    if os.name == "nt":  # pragma: no cover - native Windows CI
        from fabric_cli.provider_accounts import _windows_private_fd

        return _windows_private_fd(descriptor, apply=False)
    return stat.S_IMODE(opened.st_mode) == stat.S_IMODE(current.st_mode) == 0o600


def _write_exact_descriptor(descriptor: int, payload: bytes) -> None:
    os.lseek(descriptor, 0, os.SEEK_SET)
    os.ftruncate(descriptor, 0)
    view = memoryview(payload)
    while view:
        written = os.write(descriptor, view)
        if written <= 0:
            raise OSError("short write to private SQLite snapshot")
        view = view[written:]
    os.fsync(descriptor)

def _safe_copy_db(
    src: Path,
    dst: Path,
    *,
    _destination_fd: int | None = None,
) -> bool:
    """Copy a SQLite database safely using the backup() API.

    Handles WAL mode — produces a consistent snapshot even while
    the DB is being written to.  Falls back to raw copy on failure.
    """
    destination_fd = _destination_fd
    if destination_fd is None:
        try:
            destination_fd = _create_private_file(dst)
        except Exception as exc:
            logger.error("Could not create private SQLite staging file %s: %s", dst, exc)
            return False

    conn: sqlite3.Connection | None = None
    backup_conn: sqlite3.Connection | None = None
    try:
        conn = sqlite3.connect(f"file:{src}?mode=ro", uri=True)
        backup_conn = sqlite3.connect(":memory:")
        conn.backup(backup_conn)
        payload = backup_conn.serialize()
        if not _private_destination_matches(destination_fd, dst):
            return False
        _write_exact_descriptor(destination_fd, payload)
        return _private_destination_matches(destination_fd, dst)
    except Exception as exc:
        logger.warning("SQLite safe copy failed for %s: %s", src, exc)
        if backup_conn is not None:
            with contextlib.suppress(Exception):
                backup_conn.close()
            backup_conn = None
        if conn is not None:
            with contextlib.suppress(Exception):
                conn.close()
            conn = None
        try:
            if not _private_destination_matches(destination_fd, dst):
                return False
            with PinnedFileCapability.open(src) as capability:
                payload = capability.read_bytes()
            _write_exact_descriptor(destination_fd, payload)
            return _private_destination_matches(destination_fd, dst)
        except Exception as exc2:
            logger.error("Raw copy also failed for %s: %s", src, exc2)
            return False
    finally:
        if backup_conn is not None:
            with contextlib.suppress(Exception):
                backup_conn.close()
        if conn is not None:
            with contextlib.suppress(Exception):
                conn.close()
        if destination_fd is not None:
            with contextlib.suppress(OSError):
                os.close(destination_fd)


class BackupPublicationState(str, Enum):
    COMMITTED = "committed"
    ROLLED_BACK = "rolled_back"
    UNCERTAIN = "uncertain"


@dataclass(frozen=True)
class BackupPublicationResult:
    state: BackupPublicationState
    path: Path
    operation_id: str
    phase: str


class BackupPublicationUncertain(OSError):
    def __init__(self, result: BackupPublicationResult):
        super().__init__(f"backup publication is uncertain during {result.phase}")
        self.result = result


@dataclass
class _BackupArchiveTransaction:
    path: Path
    operation_id: str
    archive: zipfile.ZipFile | None = field(default=None, repr=False)
    result: BackupPublicationResult | None = None

    def write(self, *args, **kwargs):
        if self.archive is None:
            raise RuntimeError("backup archive is not open")
        return self.archive.write(*args, **kwargs)

    def writestr(self, *args, **kwargs):
        if self.archive is None:
            raise RuntimeError("backup archive is not open")
        return self.archive.writestr(*args, **kwargs)

    def open(self, *args, **kwargs):
        if self.archive is None:
            raise RuntimeError("backup archive is not open")
        return self.archive.open(*args, **kwargs)


class BackupSourceChanged(OSError):
    """A source changed after its exact bytes were authorized for inclusion."""


def _backup_path_is_redirect(path: Path, metadata: os.stat_result) -> bool:
    if stat.S_ISLNK(metadata.st_mode):
        return True
    reparse_flag = getattr(stat, "FILE_ATTRIBUTE_REPARSE_POINT", 0x400)
    if getattr(metadata, "st_file_attributes", 0) & reparse_flag:
        return True
    isjunction = getattr(os.path, "isjunction", None)
    if isjunction is not None:
        try:
            return bool(isjunction(path))
        except OSError:
            return True
    return False


@contextmanager
def _pinned_backup_source(path: Path) -> Iterator[PinnedFileCapability | None]:
    """Pin one non-redirect regular source and prove its requested leaf."""
    try:
        before = os.stat(path, follow_symlinks=False)
        if not stat.S_ISREG(before.st_mode) or _backup_path_is_redirect(path, before):
            yield None
            return
        capability = PinnedFileCapability.open(path)
        after = os.stat(path, follow_symlinks=False)
    except (OSError, PinnedPathError):
        yield None
        return
    identity = (before.st_dev, before.st_ino)
    if (
        identity != capability.identity
        or identity != (after.st_dev, after.st_ino)
        or _backup_path_is_redirect(path, after)
    ):
        capability.close()
        yield None
        return
    try:
        yield capability
    finally:
        capability.close()


def _backup_zip_info(
    capability: PinnedFileCapability,
    arcname: str,
) -> zipfile.ZipInfo:
    metadata = os.fstat(capability.fd)
    timestamp = time.localtime(metadata.st_mtime)
    if timestamp.tm_year < 1980 or timestamp.tm_year > 2107:
        raise ValueError("ZIP does not support this source timestamp")
    info = zipfile.ZipInfo(
        arcname.replace(os.sep, "/"),
        (timestamp.tm_year, timestamp.tm_mon, timestamp.tm_mday, timestamp.tm_hour,
         timestamp.tm_min, timestamp.tm_sec),
    )
    info.create_system = 3
    info.external_attr = (stat.S_IMODE(metadata.st_mode) & 0xFFFF) << 16
    info.compress_type = zipfile.ZIP_DEFLATED
    return info


def _write_pinned_backup_source(
    transaction: _BackupArchiveTransaction,
    path: Path,
    arcname: str,
    *,
    fabric_root: Path,
) -> int | None:
    """Classify and stream the same exact descriptor into the outer ZIP."""
    with _pinned_backup_source(path) as capability:
        if capability is None:
            return None
        try:
            private = classify_pinned_backup_archive(
                capability,
                active_home=fabric_root,
                fabric_root=fabric_root,
            )
            capability.assert_unchanged()
        except PinnedPathError:
            # Nothing has been written yet, so a pre-consumption swap is a safe
            # exclusion rather than a partial-archive failure.
            return None
        if private:
            return None
        info = _backup_zip_info(capability, arcname)
        try:
            with capability.open_reader() as source, transaction.open(
                info,
                "w",
            ) as destination:
                shutil.copyfileobj(source, destination, length=1024 * 1024)
        except PinnedPathError as exc:
            # An entry may already exist. Abort the whole transaction so bytes
            # that changed after classification can never be published.
            raise BackupSourceChanged("backup source changed during copy") from exc
        return capability.size


def _write_backup_path(
    transaction: _BackupArchiveTransaction,
    path: Path,
    arcname: str,
    *,
    fabric_root: Path,
    staging_parent: Path,
) -> int | None:
    if path.suffix != ".db":
        return _write_pinned_backup_source(
            transaction,
            path,
            arcname,
            fabric_root=fabric_root,
        )
    if _is_canonical_work_store_relative(arcname):
        with _private_work_db_staging_path(staging_parent) as temporary:
            if snapshot_work_db_to_disk(path, temporary) is None:
                raise WorkStoreSnapshotError(
                    "source_disappeared",
                    "Work store disappeared after backup enumeration",
                )
            try:
                snapshot_metadata = temporary.stat(follow_symlinks=False)
            except OSError as exc:
                raise WorkStoreSnapshotError(
                    "destination_changed",
                    "closed Work snapshot disappeared before archive streaming",
                ) from exc
            if (
                not stat.S_ISREG(snapshot_metadata.st_mode)
                or getattr(snapshot_metadata, "st_nlink", 1) != 1
            ):
                raise WorkStoreSnapshotError(
                    "destination_changed",
                    "closed Work snapshot changed before archive streaming",
                )
            snapshot_identity = (
                snapshot_metadata.st_dev,
                snapshot_metadata.st_ino,
            )
            try:
                return _write_pinned_backup_source(
                    transaction,
                    temporary,
                    arcname,
                    fabric_root=fabric_root,
                )
            finally:
                try:
                    current = temporary.stat(follow_symlinks=False)
                except OSError:
                    current = None
                if current is not None and (
                    current.st_dev,
                    current.st_ino,
                ) == snapshot_identity:
                    with contextlib.suppress(OSError):
                        temporary.unlink()

    temporary = _private_db_staging_path(staging_parent)
    try:
        if not _safe_copy_db(path, temporary):
            return None
        return _write_pinned_backup_source(
            transaction,
            temporary,
            arcname,
            fabric_root=fabric_root,
        )
    finally:
        temporary.unlink(missing_ok=True)


def _is_canonical_work_store_relative(relative: str | Path) -> bool:
    """Match only a profile-root Work store, never an unrelated nested DB."""
    parts = Path(str(relative).replace("\\", "/")).parts
    return _is_canonical_work_store_artifact_relative(relative) and (
        parts[-1].casefold() == "work.db"
    )


def _is_canonical_work_store_artifact_relative(relative: str | Path) -> bool:
    """Match Work main/sidecar/guard files at canonical profile roots."""
    parts = Path(str(relative).replace("\\", "/")).parts
    return (len(parts) == 1 and is_work_store_private_basename(parts[0])) or (
        len(parts) == 3
        and parts[0] == "profiles"
        and parts[1] not in {"", ".", ".."}
        and is_work_store_private_basename(parts[2])
    )


@dataclass(frozen=True)
class _StagedBackupProof:
    identity: tuple[int, int]
    size: int
    sha256: str


def _sha256_descriptor(descriptor: int) -> str:
    digest = hashlib.sha256()
    offset = 0
    while True:
        if hasattr(os, "pread"):
            chunk = os.pread(descriptor, 1024 * 1024, offset)
        else:  # pragma: no cover - Windows
            prior = os.lseek(descriptor, 0, os.SEEK_CUR)
            try:
                os.lseek(descriptor, offset, os.SEEK_SET)
                chunk = os.read(descriptor, 1024 * 1024)
            finally:
                os.lseek(descriptor, prior, os.SEEK_SET)
        if not chunk:
            return digest.hexdigest()
        digest.update(chunk)
        offset += len(chunk)


def _staged_descriptor_proof(descriptor: int) -> _StagedBackupProof:
    before = os.fstat(descriptor)
    if not stat.S_ISREG(before.st_mode) or getattr(before, "st_nlink", 1) != 1:
        raise OSError("staged backup descriptor is not one private regular file")
    sha256 = _sha256_descriptor(descriptor)
    after = os.fstat(descriptor)
    if (
        (
            before.st_dev,
            before.st_ino,
            before.st_size,
            getattr(before, "st_mtime_ns", before.st_mtime),
            getattr(before, "st_ctime_ns", before.st_ctime),
        )
        != (
            after.st_dev,
            after.st_ino,
            after.st_size,
            getattr(after, "st_mtime_ns", after.st_mtime),
            getattr(after, "st_ctime_ns", after.st_ctime),
        )
        or getattr(after, "st_nlink", 1) != 1
    ):
        raise OSError("staged backup changed while proving bytes")
    return _StagedBackupProof(
        identity=(after.st_dev, after.st_ino),
        size=after.st_size,
        sha256=sha256,
    )


def _path_matches_staged_proof(
    path: Path,
    proof: _StagedBackupProof,
    *,
    parent_fd: int | None,
) -> bool:
    if (
        _path_identity(path, parent_fd=parent_fd) != proof.identity
        or _path_size(path, parent_fd=parent_fd) != proof.size
    ):
        return False
    try:
        with PinnedFileCapability.open(path) as capability:
            opened = os.fstat(capability.fd)
            if (
                capability.identity != proof.identity
                or capability.size != proof.size
                or getattr(opened, "st_nlink", 1) != 1
            ):
                return False
            return _sha256_descriptor(capability.fd) == proof.sha256
    except PinnedPathError:
        return False


def _path_identity(
    path: Path,
    *,
    parent_fd: int | None = None,
) -> tuple[int, int] | None:
    try:
        if os.name != "nt" and parent_fd is not None:
            metadata = os.stat(path.name, dir_fd=parent_fd, follow_symlinks=False)
        else:
            metadata = path.stat(follow_symlinks=False)
    except (FileNotFoundError, OSError):
        return None
    if not stat.S_ISREG(metadata.st_mode) or stat.S_ISLNK(metadata.st_mode):
        return None
    return metadata.st_dev, metadata.st_ino


def _path_size(path: Path, *, parent_fd: int | None = None) -> int | None:
    try:
        if os.name != "nt" and parent_fd is not None:
            metadata = os.stat(path.name, dir_fd=parent_fd, follow_symlinks=False)
        else:
            metadata = path.stat(follow_symlinks=False)
    except (FileNotFoundError, OSError):
        return None
    if not stat.S_ISREG(metadata.st_mode) or stat.S_ISLNK(metadata.st_mode):
        return None
    return metadata.st_size


def _publication_journal_path(registry_root: Path, out_path: Path) -> Path:
    identity_path = os.path.normcase(os.path.abspath(os.fspath(out_path)))
    key = hashlib.sha256(identity_path.encode("utf-8")).hexdigest()[:32]
    return registry_root / _PRIVATE_BACKUP_JOURNAL_DIR / f"{key}.json"


def _publication_thread_lock(path: Path) -> threading.RLock:
    key = f"{os.getpid()}:{os.path.normcase(os.path.abspath(os.fspath(path)))}"
    with _PUBLICATION_THREAD_LOCKS_GUARD:
        return _PUBLICATION_THREAD_LOCKS.setdefault(key, threading.RLock())


@contextmanager
def _publication_lock(registry_root: Path, out_path: Path) -> Iterator[None]:
    """Serialize recovery/publication for one target across threads/processes."""
    journal = _publication_journal_path(registry_root, out_path)
    lock_path = journal.with_suffix(".lock")
    thread_lock = _publication_thread_lock(lock_path)
    with thread_lock:
        descriptor: int | None = None
        directory_descriptor: int | None = None
        transaction_descriptor: int | None = None
        try:
            if os.name == "nt":  # pragma: no cover - Windows CI
                import msvcrt

                from fabric_cli.provider_accounts import (
                    _windows_open_private_file,
                    _windows_private_directory,
                )

                lock_path.parent.mkdir(parents=True, exist_ok=True, mode=0o700)
                if not _windows_private_directory(lock_path.parent, apply=True):
                    raise PermissionError("backup transaction directory is not private")
                descriptor = _windows_open_private_file(
                    lock_path,
                    create_new=False,
                )
                if os.fstat(descriptor).st_size == 0:
                    os.write(descriptor, b"\0")
                    os.fsync(descriptor)
                os.lseek(descriptor, 0, os.SEEK_SET)
                msvcrt.locking(descriptor, msvcrt.LK_LOCK, 1)
            else:
                import fcntl

                registry_root.mkdir(parents=True, exist_ok=True, mode=0o700)
                directory_flags = os.O_RDONLY | getattr(os, "O_DIRECTORY", 0)
                directory_flags |= getattr(os, "O_CLOEXEC", 0)
                directory_flags |= getattr(os, "O_NOFOLLOW", 0)
                directory_descriptor = os.open(registry_root, directory_flags)
                opened_directory = os.fstat(directory_descriptor)
                current_directory = os.stat(
                    registry_root,
                    follow_symlinks=False,
                )
                if (
                    not stat.S_ISDIR(opened_directory.st_mode)
                    or (opened_directory.st_dev, opened_directory.st_ino)
                    != (current_directory.st_dev, current_directory.st_ino)
                ):
                    raise PermissionError("backup registry authority changed")
                # Serialize on the pinned registry-root inode. The transaction
                # directory and marker beneath it are attacker-replaceable;
                # contenders must not open either until this stable authority
                # has been acquired and revalidated.
                fcntl.flock(directory_descriptor, fcntl.LOCK_EX)
                current_directory = os.stat(registry_root, follow_symlinks=False)
                if (opened_directory.st_dev, opened_directory.st_ino) != (
                    current_directory.st_dev,
                    current_directory.st_ino,
                ):
                    raise PermissionError("backup registry authority changed")
                lock_path.parent.mkdir(parents=True, exist_ok=True, mode=0o700)
                lock_path.parent.chmod(0o700)
                transaction_descriptor = os.open(
                    lock_path.parent,
                    directory_flags,
                )
                opened_transaction = os.fstat(transaction_descriptor)
                current_transaction = os.stat(
                    lock_path.parent,
                    follow_symlinks=False,
                )
                if (
                    not stat.S_ISDIR(opened_transaction.st_mode)
                    or (opened_transaction.st_dev, opened_transaction.st_ino)
                    != (current_transaction.st_dev, current_transaction.st_ino)
                    or stat.S_IMODE(opened_transaction.st_mode) != 0o700
                ):
                    raise PermissionError("backup transaction directory changed")
                flags = os.O_RDWR | os.O_CREAT
                flags |= getattr(os, "O_CLOEXEC", 0)
                flags |= getattr(os, "O_NOFOLLOW", 0)
                descriptor = os.open(
                    lock_path.name,
                    flags,
                    0o600,
                    dir_fd=transaction_descriptor,
                )
                os.fchmod(descriptor, 0o600)
                opened = os.fstat(descriptor)
                current = os.stat(
                    lock_path.name,
                    dir_fd=transaction_descriptor,
                    follow_symlinks=False,
                )
                if (
                    not stat.S_ISREG(opened.st_mode)
                    or opened.st_nlink != 1
                    or stat.S_IMODE(opened.st_mode) != 0o600
                    or (opened.st_dev, opened.st_ino)
                    != (current.st_dev, current.st_ino)
                ):
                    raise PermissionError("backup transaction lock is redirected")
                current = os.stat(
                    lock_path.name,
                    dir_fd=transaction_descriptor,
                    follow_symlinks=False,
                )
                if (opened.st_dev, opened.st_ino) != (
                    current.st_dev,
                    current.st_ino,
                ):
                    raise PermissionError("backup transaction lock changed")

            yield
        finally:
            if descriptor is not None:
                if os.name == "nt":  # pragma: no cover - Windows CI
                    import msvcrt

                    with contextlib.suppress(OSError):
                        os.lseek(descriptor, 0, os.SEEK_SET)
                        msvcrt.locking(descriptor, msvcrt.LK_UNLCK, 1)
                with contextlib.suppress(OSError):
                    os.close(descriptor)
            if transaction_descriptor is not None:
                with contextlib.suppress(OSError):
                    os.close(transaction_descriptor)
            if directory_descriptor is not None:
                import fcntl

                with contextlib.suppress(OSError):
                    fcntl.flock(directory_descriptor, fcntl.LOCK_UN)
                with contextlib.suppress(OSError):
                    os.close(directory_descriptor)


def _write_publication_journal(
    journal: Path,
    *,
    operation_id: str,
    target: Path,
    temporary: Path,
    staged_proof: _StagedBackupProof,
) -> None:
    payload = json.dumps(
        {
            "version": 2,
            "operation_id": operation_id,
            "target": os.fspath(target),
            "temporary": os.fspath(temporary),
            "staged_device": staged_proof.identity[0],
            "staged_inode": staged_proof.identity[1],
            "staged_size": staged_proof.size,
            "staged_sha256": staged_proof.sha256,
            "phase": "prepared",
        },
        sort_keys=True,
        separators=(",", ":"),
    ).encode("utf-8")
    _write_private_record(journal, payload)


def _remove_publication_journal(journal: Path) -> None:
    try:
        journal.unlink(missing_ok=True)
    finally:
        if journal.parent.exists():
            _fsync_directory(journal.parent)


def _replace_private_backup(
    temporary: Path,
    target: Path,
    *,
    parent_fd: int | None,
) -> None:
    if os.name == "nt":  # pragma: no cover - Windows CI
        from fabric_cli.provider_accounts import _windows_move_write_through

        _windows_move_write_through(
            temporary,
            target,
            replace_existing=target.exists(),
        )
        return
    if parent_fd is None:
        raise OSError("output parent is not pinned")
    _assert_output_parent_unchanged(target.parent, parent_fd)
    os.replace(
        temporary.name,
        target.name,
        src_dir_fd=parent_fd,
        dst_dir_fd=parent_fd,
    )


def _assert_output_parent_unchanged(parent: Path, parent_fd: int | None) -> None:
    if os.name == "nt":
        # The Windows directory handle held by _pinned_output_parent omits
        # FILE_SHARE_DELETE, so rename/delete is blocked by the kernel.
        return
    if parent_fd is None:
        raise OSError("output parent is not pinned")
    opened_parent = os.fstat(parent_fd)
    current_parent = os.stat(parent, follow_symlinks=False)
    if (
        not stat.S_ISDIR(opened_parent.st_mode)
        or stat.S_ISLNK(current_parent.st_mode)
        or (opened_parent.st_dev, opened_parent.st_ino)
        != (current_parent.st_dev, current_parent.st_ino)
    ):
        raise OSError("output parent changed before publication")


def _fsync_output_parent(parent: Path, parent_fd: int | None) -> None:
    if os.name == "nt":
        return
    if parent_fd is None:
        raise OSError("output parent is not pinned")
    os.fsync(parent_fd)


def _cleanup_staged_path(
    path: Path,
    *,
    parent_fd: int | None,
    expected_identity: tuple[int, int] | None = None,
) -> None:
    if (
        expected_identity is not None
        and _path_identity(path, parent_fd=parent_fd) != expected_identity
    ):
        return
    try:
        if os.name == "nt" or parent_fd is None:
            path.unlink(missing_ok=True)
        else:
            os.unlink(path.name, dir_fd=parent_fd)
    except FileNotFoundError:
        pass


def _reconcile_publication(
    *,
    out_path: Path,
    registry_root: Path,
    parent_fd: int | None = None,
) -> BackupPublicationResult | None:
    journal = _publication_journal_path(registry_root, out_path)
    try:
        with PinnedFileCapability.open(journal) as journal_capability:
            payload = json.loads(
                journal_capability.read_bytes(max_bytes=16_384).decode("utf-8")
            )
        if not isinstance(payload, dict):
            raise ValueError("journal payload is not an object")
        payload_target = os.path.normcase(
            os.path.abspath(os.fspath(payload.get("target")))
        )
        expected_target = os.path.normcase(os.path.abspath(os.fspath(out_path)))
        if payload.get("version") != 2 or payload_target != expected_target:
            raise ValueError("journal target mismatch")
        operation_id = str(payload["operation_id"])
        temporary = Path(payload["temporary"])
        staged_proof = _StagedBackupProof(
            identity=(
                int(payload["staged_device"]),
                int(payload["staged_inode"]),
            ),
            size=int(payload["staged_size"]),
            sha256=str(payload["staged_sha256"]),
        )
        if (
            len(operation_id) != 32
            or any(character not in "0123456789abcdef" for character in operation_id)
            or temporary.parent != out_path.parent
            or not temporary.name.startswith(_PRIVATE_BACKUP_TEMP_PREFIX)
            or not temporary.name.endswith(f"-{operation_id}.zip")
            or staged_proof.identity[0] < 0
            or staged_proof.identity[1] < 0
            or staged_proof.size < 0
            or len(staged_proof.sha256) != 64
            or any(
                character not in "0123456789abcdef"
                for character in staged_proof.sha256
            )
        ):
            raise ValueError("journal staging identity mismatch")
    except PinnedPathError as exc:
        if exc.code == "not_found":
            return None
        result = BackupPublicationResult(
            BackupPublicationState.UNCERTAIN,
            out_path,
            "unknown",
            "reconcile_journal",
        )
        raise BackupPublicationUncertain(result) from exc
    except (OSError, UnicodeError, ValueError, KeyError, TypeError) as exc:
        result = BackupPublicationResult(
            BackupPublicationState.UNCERTAIN,
            out_path,
            "unknown",
            "reconcile_journal",
        )
        raise BackupPublicationUncertain(result) from exc

    target_identity = _path_identity(out_path, parent_fd=parent_fd)
    temporary_identity = _path_identity(temporary, parent_fd=parent_fd)
    if (
        target_identity == staged_proof.identity
        and _path_matches_staged_proof(
            out_path,
            staged_proof,
            parent_fd=parent_fd,
        )
        and temporary_identity != staged_proof.identity
    ):
        try:
            if parent_fd is None:
                _fsync_directory(out_path.parent)
            else:
                _fsync_output_parent(out_path.parent, parent_fd)
                _assert_output_parent_unchanged(out_path.parent, parent_fd)
            register_private_backup(out_path, fabric_root=registry_root)
            if not _path_matches_staged_proof(
                out_path,
                staged_proof,
                parent_fd=parent_fd,
            ):
                raise OSError("reconciled backup changed during registration")
            _remove_publication_journal(journal)
        except BaseException as exc:
            result = BackupPublicationResult(
                BackupPublicationState.UNCERTAIN,
                out_path,
                operation_id,
                "reconcile_commit",
            )
            raise BackupPublicationUncertain(result) from exc
        return BackupPublicationResult(
            BackupPublicationState.COMMITTED,
            out_path,
            operation_id,
            "reconciled_commit",
        )
    if (
        temporary_identity == staged_proof.identity
        and _path_matches_staged_proof(
            temporary,
            staged_proof,
            parent_fd=parent_fd,
        )
        and target_identity != staged_proof.identity
    ):
        try:
            _cleanup_staged_path(
                temporary,
                parent_fd=parent_fd,
                expected_identity=staged_proof.identity,
            )
            if parent_fd is None:
                _fsync_directory(temporary.parent)
            else:
                _fsync_output_parent(temporary.parent, parent_fd)
                _assert_output_parent_unchanged(temporary.parent, parent_fd)
            _remove_publication_journal(journal)
        except BaseException as exc:
            result = BackupPublicationResult(
                BackupPublicationState.UNCERTAIN,
                out_path,
                operation_id,
                "reconcile_rollback",
            )
            raise BackupPublicationUncertain(result) from exc
        return BackupPublicationResult(
            BackupPublicationState.ROLLED_BACK,
            out_path,
            operation_id,
            "reconciled_rollback",
        )
    result = BackupPublicationResult(
        BackupPublicationState.UNCERTAIN,
        out_path,
        operation_id,
        "reconcile_identity",
    )
    raise BackupPublicationUncertain(result)


@contextmanager
def _pinned_output_parent(out_path: Path) -> Iterator[tuple[Path, int | None]]:
    parent = out_path.parent.resolve(strict=True)
    target = parent / out_path.name
    if os.name == "nt":  # pragma: no cover - Windows CI
        from fabric_cli.provider_accounts import (
            _windows_close_handle,
            _windows_pin_directory_tree,
        )

        handles, _identity = _windows_pin_directory_tree(parent)
        try:
            yield target, None
        finally:
            for handle in reversed(handles):
                _windows_close_handle(handle)
        return
    flags = os.O_RDONLY | getattr(os, "O_DIRECTORY", 0)
    flags |= getattr(os, "O_CLOEXEC", 0)
    flags |= getattr(os, "O_NOFOLLOW", 0)
    descriptor = os.open(parent, flags)
    try:
        opened = os.fstat(descriptor)
        current = os.stat(parent, follow_symlinks=False)
        if (
            not stat.S_ISDIR(opened.st_mode)
            or (opened.st_dev, opened.st_ino) != (current.st_dev, current.st_ino)
        ):
            raise OSError("output parent changed")
        yield target, descriptor
    finally:
        os.close(descriptor)


def _open_private_staging_file(
    path: Path,
    *,
    parent_fd: int | None,
) -> int:
    if os.name == "nt":  # pragma: no cover - Windows CI
        from fabric_cli.provider_accounts import _windows_open_private_file

        return _windows_open_private_file(
            path,
            create_new=True,
            share_delete=True,
        )
    if parent_fd is None:
        raise OSError("output parent is not pinned")
    flags = os.O_RDWR | os.O_CREAT | os.O_EXCL
    flags |= getattr(os, "O_CLOEXEC", 0)
    flags |= getattr(os, "O_NOFOLLOW", 0)
    descriptor = os.open(path.name, flags, 0o600, dir_fd=parent_fd)
    os.fchmod(descriptor, 0o600)
    opened = os.fstat(descriptor)
    if (
        not stat.S_ISREG(opened.st_mode)
        or opened.st_nlink != 1
        or stat.S_IMODE(opened.st_mode) != 0o600
    ):
        os.close(descriptor)
        raise PermissionError("backup staging file is not private")
    return descriptor


def _commit_private_backup(
    *,
    transaction: _BackupArchiveTransaction,
    temporary: Path,
    target: Path,
    registry_root: Path,
    parent_fd: int | None,
    staged_descriptor: int | None = None,
    staged_proof: _StagedBackupProof | None = None,
) -> BackupPublicationResult:
    descriptor_proof = (
        _staged_descriptor_proof(staged_descriptor)
        if staged_descriptor is not None
        else None
    )
    if staged_proof is None:
        staged_proof = descriptor_proof
    elif descriptor_proof is not None and descriptor_proof != staged_proof:
        raise OSError("staged backup descriptor bytes changed before commit")
    _assert_output_parent_unchanged(target.parent, parent_fd)
    if staged_proof is None or not _path_matches_staged_proof(
        temporary,
        staged_proof,
        parent_fd=parent_fd,
    ):
        raise OSError("staged backup path no longer names the proven bytes")

    def committed_bytes_match() -> bool:
        if not _path_matches_staged_proof(
            target,
            staged_proof,
            parent_fd=parent_fd,
        ):
            return False
        return (
            staged_descriptor is None
            or _staged_descriptor_proof(staged_descriptor) == staged_proof
        )

    journal = _publication_journal_path(registry_root, target)
    _write_publication_journal(
        journal,
        operation_id=transaction.operation_id,
        target=target,
        temporary=temporary,
        staged_proof=staged_proof,
    )
    try:
        _replace_private_backup(temporary, target, parent_fd=parent_fd)
    except BaseException as exc:
        if committed_bytes_match():
            try:
                _fsync_output_parent(target.parent, parent_fd)
                _assert_output_parent_unchanged(target.parent, parent_fd)
                if not committed_bytes_match():
                    raise OSError("published backup changed during recovery")
                register_private_backup(target, fabric_root=registry_root)
                _assert_output_parent_unchanged(target.parent, parent_fd)
                if not committed_bytes_match():
                    raise OSError("published backup changed during registration")
                _remove_publication_journal(journal)
            except BaseException as finish_exc:
                result = BackupPublicationResult(
                    BackupPublicationState.UNCERTAIN,
                    target,
                    transaction.operation_id,
                    "post_replace_recovery",
                )
                raise BackupPublicationUncertain(result) from finish_exc
            return BackupPublicationResult(
                BackupPublicationState.COMMITTED,
                target,
                transaction.operation_id,
                "post_replace_recovered",
            )
        if _path_matches_staged_proof(
            temporary,
            staged_proof,
            parent_fd=parent_fd,
        ):
            try:
                _remove_publication_journal(journal)
            except BaseException as cleanup_exc:
                result = BackupPublicationResult(
                    BackupPublicationState.UNCERTAIN,
                    target,
                    transaction.operation_id,
                    "rollback_journal",
                )
                raise BackupPublicationUncertain(result) from cleanup_exc
            result = BackupPublicationResult(
                BackupPublicationState.ROLLED_BACK,
                target,
                transaction.operation_id,
                "replace",
            )
            with contextlib.suppress(Exception):
                setattr(exc, "backup_publication_result", result)
            raise
        result = BackupPublicationResult(
            BackupPublicationState.UNCERTAIN,
            target,
            transaction.operation_id,
            "replace_identity",
        )
        raise BackupPublicationUncertain(result) from exc

    try:
        _fsync_output_parent(target.parent, parent_fd)
        _assert_output_parent_unchanged(target.parent, parent_fd)
        if not committed_bytes_match():
            raise OSError("published backup changed before registration")
        register_private_backup(target, fabric_root=registry_root)
        _assert_output_parent_unchanged(target.parent, parent_fd)
        if not committed_bytes_match():
            raise OSError("published backup changed during registration")
    except BaseException as exc:
        result = BackupPublicationResult(
            BackupPublicationState.UNCERTAIN,
            target,
            transaction.operation_id,
            "durability_or_registry",
        )
        raise BackupPublicationUncertain(result) from exc
    with contextlib.suppress(OSError):
        _remove_publication_journal(journal)
    return BackupPublicationResult(
        BackupPublicationState.COMMITTED,
        target,
        transaction.operation_id,
        "committed",
    )


@contextmanager
def _private_backup_zip(
    out_path: Path,
    *,
    registry_root: Path,
) -> Iterator[_BackupArchiveTransaction]:
    """Build, journal, and publish one full backup as a typed transaction."""
    operation_id = uuid.uuid4().hex
    transaction = _BackupArchiveTransaction(out_path, operation_id)
    registry_root = registry_root.resolve(strict=False)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with _pinned_output_parent(out_path) as (target, parent_fd), _publication_lock(
        registry_root,
        target,
    ):
        transaction.path = target
        try:
            reconciled = _reconcile_publication(
                out_path=target,
                registry_root=registry_root,
                parent_fd=parent_fd,
            )
        except BackupPublicationUncertain as exc:
            transaction.result = exc.result
            raise
        if reconciled is not None and reconciled.state is BackupPublicationState.UNCERTAIN:
            transaction.result = reconciled
            raise BackupPublicationUncertain(reconciled)
        temporary = target.with_name(
            f"{_PRIVATE_BACKUP_TEMP_PREFIX}{os.getpid()}-{operation_id}.zip"
        )
        descriptor: int | None = None
        staging_identity: tuple[int, int] | None = None
        try:
            descriptor = _open_private_staging_file(
                temporary,
                parent_fd=parent_fd,
            )
            opened_staging = os.fstat(descriptor)
            staging_identity = (opened_staging.st_dev, opened_staging.st_ino)
            raw_descriptor = os.dup(descriptor)
            with os.fdopen(raw_descriptor, "w+b") as raw_archive:
                with zipfile.ZipFile(
                    raw_archive,
                    "w",
                    zipfile.ZIP_DEFLATED,
                    compresslevel=6,
                ) as backup_zip:
                    transaction.archive = backup_zip
                    yield transaction
                    backup_zip.writestr(PRIVATE_BACKUP_ZIP_ENTRY, b"1\n")
                    backup_zip.comment = PRIVATE_BACKUP_ZIP_COMMENT
                transaction.archive = None
                raw_archive.flush()
                os.fsync(raw_archive.fileno())

            staged_proof = _staged_descriptor_proof(descriptor)
            transaction.result = _commit_private_backup(
                transaction=transaction,
                temporary=temporary,
                target=target,
                registry_root=registry_root,
                parent_fd=parent_fd,
                staged_descriptor=descriptor,
                staged_proof=staged_proof,
            )
        except BaseException as exc:
            if transaction.result is None:
                if isinstance(exc, BackupPublicationUncertain):
                    transaction.result = exc.result
                else:
                    classified = getattr(exc, "backup_publication_result", None)
                    transaction.result = classified or BackupPublicationResult(
                        BackupPublicationState.ROLLED_BACK,
                        target,
                        operation_id,
                        "write",
                    )
            raise
        finally:
            transaction.archive = None
            if descriptor is not None:
                with contextlib.suppress(OSError):
                    os.close(descriptor)
            if (
                transaction.result is None
                or transaction.result.state is not BackupPublicationState.UNCERTAIN
            ):
                _cleanup_staged_path(
                    temporary,
                    parent_fd=parent_fd,
                    expected_identity=staging_identity,
                )


