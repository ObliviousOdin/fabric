def _should_skip_backup_file(
    abs_path: Path,
    rel_path: Path,
    out_path: Path,
    fabric_root: Path,
) -> bool:
    """Return True when a candidate file should not be written to a backup zip."""
    if _should_exclude(rel_path):
        return True

    # zipfile.write() follows file symlinks, so skip links before any archive
    # write can copy data from outside FABRIC_HOME.
    if abs_path.is_symlink():
        return True

    # A prior full backup remains private after arbitrary renames, copies, or
    # comment stripping.  The bounded classifier uses the protected identity
    # registry plus archive member structure; it does not trust basenames.
    if is_private_backup_archive(
        abs_path,
        active_home=fabric_root,
        fabric_root=fabric_root,
    ):
        return True

    try:
        return abs_path.resolve() == out_path.resolve()
    except (OSError, ValueError):
        return False


